# String fuctions

str = "Rock the world"
print(str.upper()) #Returns string in uppercase
print(str.lower()) #Returns string in lowercase
print(str.capitalize) #Returns string in capitalized form
print(str.title()) #Returns string in title case
print(str.swapcase()) #Swaps cases, lower case becomes upper case and vice versa
print(str.isalnum()) #Checks if string contain alphabets and numbers
print(str.isalpha()) #Checks if string is alphabets only
print(str.islower()) #checks if string is in lowercase
print(str.isupper()) #checks if string is in uppercase