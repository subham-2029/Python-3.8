# Mathematical Fucntions

import math # Importing the math module to access mathematical functions
print(math.sqrt(16)) # Output: 4.0
print(math.factorial(5)) # Output: 120
print(math.gcd(48, 18)) # Output: 6
print(math.sin(math.pi/2)) # Output: 1.0
print(math.log(100, 10)) # Output: 2.0
print(math.pow(2, 3)) # Output: 8.0
print(math.ceil(4.2)) # Output: 5
print(math.floor(4.7)) # Output: 4

import cmath # Importing the cmath module to access complex number functions
print(cmath.sqrt(-1)) # Output: 1j
print(cmath.phase(1 + 1j)) # Output: 0.785398
print(cmath.polar(1 + 1j)) # Output: (1.4142135623730951, 0.7853981633974483)
print(cmath.rect(1, math.pi/4)) # Output: (0.7071067811865476+0.7071067811865475j)